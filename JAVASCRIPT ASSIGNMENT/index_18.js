// Printing Original Array
var hello = ["Karachi", "Lahore", "Islamabad", "Peshawar", "Rawalpindi"];
console.log(hello);  

// Printing array in alphabetical order
var newArray = [...hello];
newArray.sort();
console.log(newArray);    

// Printing Original Array
console.log(hello);   

// Printing reverse alphabetical array 
var reverseArray = newArray.reverse();
console.log(reverseArray.sort().reverse());  

// Printing Original Array
console.log(hello);   

// Printing reverse array
console.log(hello.reverse())     

// Printing Original Array
console.log(hello.reverse());  

// Printing array in alphabetical order
console.log(hello.sort())

// Printing reverse array
console.log(hello.reverse());




